#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	count;

	count = 0;
	if (s && f)
	{
		while (s[count])
		{
			(*f)(count, &s[count]);
			count++;
		}
	}
}
